function [Dim]=DimentionFunction(FunNum)

if  FunNum==14
   Dim=2;
elseif FunNum==15
   Dim=4;
elseif FunNum==16
    Dim=2;
elseif FunNum==17
    Dim=2;
elseif FunNum==18
    Dim=2;
elseif FunNum==19
    Dim=3;
elseif FunNum==20
    Dim=6;
elseif FunNum==21
    Dim=4;
elseif FunNum==22
    Dim=4;
elseif FunNum==23
    Dim=4;
elseif FunNum==1000
    Dim=9;
else
    Dim=30;
end